1. Полная информация о заказах с данными модели и продавца
SELECT 
    №_заказа,
    ФИО AS Клиент,
    Модель,
    Цвет,
    Сумма_заказа,
    ФИО AS Продавец,
    Город AS Город_продавца
FROM Клиенты 
JOIN Модели м ON Код_модели = Код_модели
JOIN Продавцы п ON №_продавца = №_продавца;

2. Продавцы и их общий объем продаж
SELECT 
    п.ФИО,
    SUM(к.Сумма_заказа) AS Общий_объем,
    SUM(к.Сумма_заказа * п.Комиссионные) AS Заработок
FROM Продавцы п
JOIN Клиенты к ON п.№_продавца = к.№_продавца
GROUP BY п.ФИО;
